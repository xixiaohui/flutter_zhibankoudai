void main() {
  final enFile = File('assets/cloudData/modules/modules_en.json');
  final enModules = jsonDecode(enFile.readAsStringSync()) as List<dynamic>;
  print('Read ${enModules.length} modules from modules_en.json');

  for (final locale in langData.keys) {
    // Merge common words into the language-specific dictionary
    final words = Map<String, String>.from(langData[locale]!);
    if (commonWords.containsKey(locale)) {
      words.addAll(commonWords[locale]!);
    }
    // Sort keys by length descending so phrase matches take priority
    final sortedKeys = words.keys.toList()
      ..sort((a, b) => b.length.compareTo(a.length));

    int totalTranslated = 0;
    int totalFields = 0;

    final translated = enModules.map((m) {
      final module = Map<String, dynamic>.from(m as Map);

      for (final field in translatableFields) {
        if (module[field] is String) {
          totalFields++;
          var text = module[field] as String;
          final original = text;
          for (final key in sortedKeys) {
            text = text.replaceAll(key, words[key]!);
          }
          if (text != original) totalTranslated++;
          module[field] = text;
        }
      }

      // Translate aiTags using exact-match lookup
      if (module['aiTags'] is List) {
        final tags = module['aiTags'] as List;
        module['aiTags'] = tags.map((t) {
          final s = t.toString();
          // Try exact match (case-insensitive)
          for (final key in sortedKeys) {
            if (key.toLowerCase() == s.toLowerCase()) {
              return words[key];
            }
          }
          return s; // keep original
        }).toList();
      }

      return module;
    }).toList();

    final outFile = File('assets/cloudData/modules/modules_$locale.json');
    outFile.writeAsStringSync(
      const JsonEncoder.withIndent('  ').convert(translated),
    );
    print('Generated modules_$locale.json '
        '($translated.length modules, $totalTranslated/$totalFields string fields replaced)');
